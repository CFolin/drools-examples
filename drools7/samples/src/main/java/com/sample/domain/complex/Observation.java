package com.sample.domain.complex;

public class Observation {

    private long val;
    
    private long per;
    
    public Observation() {
        
    }

    public long getVal() {
        return val;
    }

    public void setVal(long val) {
        this.val = val;
    }

    public long getPer() {
        return per;
    }

    public void setPer(long per) {
        this.per = per;
    }
    
}
